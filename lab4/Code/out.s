  lw $ra, 0($sp)
  addi $sp, $sp, 4
  move $t7, $v0
  mul $s0, $t3, $t7
  move $v0, $s0
  jr $ra
label2:

main:
  addi $sp, $sp, -4
  sw $ra, 0($sp)
  jal read
  lw $ra, 0($sp)
  addi $sp, $sp, 4
  move $t0, $v0
  move $t1, $t0
  move $t2, $t1
  li $t3, 1
  bgt $t2, $t3, label3
  j label4
label3:
  move $t4, $t1
  addi $sp, $sp, -4
  sw $ra, 0($sp)
  addi $sp, $sp, -72
  sw $t0, 0($sp)
  sw $t1, 4($sp)
